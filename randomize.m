function randomize(n)
for i=1:n
    num=randi(12);
    switch num
        case 1
            F;
        case 2
            Fi;
        case 3
            R;
        case 4
            Ri;
        case 5
            L;
        case 6
            Li;
        case 7
            U;
        case 8
            Ui;
        case 9
            D;
        case 10
            Di;
        case 11
            B;
        case 12
            Bi;
                                 
    end
    
end

end
